import { spawn } from "node:child_process";
import path from "node:path";
import { fileURLToPath } from "node:url";

const API_BASE = process.env.API_BASE_URL ?? "http://localhost:8081";
const __dirname = path.dirname(fileURLToPath(import.meta.url));
const uiRoot = path.resolve(__dirname, "..");
const repoRoot = path.resolve(__dirname, "../..");

const config = {
  initialWorkers: intEnv("AUTOSCALER_DEMO_INITIAL_WORKERS", 3),
  targetWorkers: intEnv("AUTOSCALER_DEMO_TARGET_WORKERS", 5),
  slotsPerWorker: intEnv("AUTOSCALER_DEMO_SLOTS_PER_WORKER", 18),
  slotThreshold: intEnv("AUTOSCALER_DEMO_SLOT_THRESHOLD", 15),
  projects: intEnv("AUTOSCALER_DEMO_PROJECTS", 0),
  instancesPerProject: intEnv("AUTOSCALER_DEMO_INSTANCES_PER_PROJECT", 2),
  concurrency: intEnv("AUTOSCALER_DEMO_CONCURRENCY", 24),
  requests: intEnv("AUTOSCALER_DEMO_REQUESTS", 160),
  timeoutMs: intEnv("AUTOSCALER_DEMO_TIMEOUT_MS", 20_000),
  waitScaleMs: intEnv("AUTOSCALER_DEMO_WAIT_SCALE_MS", 90_000)
};

const runId = `${Date.now()}-${Math.random().toString(36).slice(2, 8)}`;
let token = "";
let autoscaler = null;

function intEnv(name, fallback) {
  const value = Number.parseInt(process.env[name] ?? "", 10);
  return Number.isFinite(value) && value > 0 ? value : fallback;
}

function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}

function assert(condition, message, details = undefined) {
  if (!condition) {
    const suffix = details === undefined ? "" : `\n${JSON.stringify(details, null, 2)}`;
    throw new Error(`${message}${suffix}`);
  }
}

async function api(pathOrUrl, options = {}) {
  const url = pathOrUrl.startsWith("http") ? pathOrUrl : `${API_BASE}${pathOrUrl}`;
  const controller = new AbortController();
  const timeout = setTimeout(() => controller.abort(), config.timeoutMs);
  try {
    const response = await fetch(url, {
      method: options.method ?? "GET",
      headers: {
        ...(options.body === undefined ? {} : { "Content-Type": "application/json" }),
        ...(options.token ? { Authorization: `Bearer ${options.token}` } : {}),
        ...(options.headers ?? {})
      },
      body: options.body === undefined ? undefined : JSON.stringify(options.body),
      signal: controller.signal
    });
    const text = await response.text();
    let body = null;
    if (text) {
      try {
        body = JSON.parse(text);
      } catch {
        body = text;
      }
    }
    if (!response.ok) {
      throw new Error(`${options.method ?? "GET"} ${url} -> ${response.status}: ${text.slice(0, 500)}`);
    }
    return body;
  } finally {
    clearTimeout(timeout);
  }
}

async function waitForHealth() {
  const startedAt = Date.now();
  while (Date.now() - startedAt < 60_000) {
    try {
      await api("/actuator/health");
      return;
    } catch {
      await sleep(1_000);
    }
  }
  throw new Error("Timed out waiting for server health");
}

function runDockerCompose(args) {
  return new Promise((resolve, reject) => {
    const child = spawn("docker", ["compose", ...args], {
      cwd: repoRoot,
      stdio: "inherit",
      shell: process.platform === "win32"
    });
    child.on("error", reject);
    child.on("exit", (code) => {
      if (code === 0) {
        resolve();
        return;
      }
      reject(new Error(`docker compose ${args.join(" ")} exited with ${code}`));
    });
  });
}

async function registerUser() {
  const username = `autoscale_demo_${runId}`.replace(/[^A-Za-z0-9_.-]/g, "_").slice(0, 32);
  const auth = await api("/api/auth/register", {
    method: "POST",
    body: {
      email: `autoscale-demo-${runId}@example.com`,
      username,
      password: "password"
    }
  });
  token = auth.token;
  return auth.user;
}

function spec(index) {
  return `
openapi: 3.0.3
info:
  title: Autoscaler Demo ${index}
  version: 1.0.0
paths:
  /orders:
    get:
      operationId: listOrders
      responses:
        "200":
          description: OK
          content:
            application/json:
              example:
                source: autoscaler-demo
                tenant: "{{query.tenant}}"
                trace: "{{header.x-trace-id}}"
`;
}

async function createTargets() {
  const targets = [];
  const targetSlots = config.initialWorkers * config.slotsPerWorker;
  const projectCount = config.projects > 0
    ? config.projects
    : Math.ceil(targetSlots / config.instancesPerProject);
  for (let projectIndex = 0; projectIndex < projectCount && targets.length < targetSlots; projectIndex += 1) {
    const project = await api("/api/projects", {
      method: "POST",
      token,
      body: {
        name: `Autoscaler Demo ${runId} ${projectIndex + 1}`,
        description: "Generated by runtime-autoscaler-demo.mjs"
      }
    });
    const version = await api(`/api/projects/${project.id}/spec-versions`, {
      method: "POST",
      token,
      body: {
        name: `autoscaler-demo-${projectIndex + 1}.yaml`,
        source: spec(projectIndex + 1)
      }
    });
    assert(version.status === "VALID", "Demo spec must be valid", version);
    for (let instanceIndex = 0; instanceIndex < config.instancesPerProject && targets.length < targetSlots; instanceIndex += 1) {
      const instance = await api(`/api/spec-versions/${version.id}/publish`, {
        method: "POST",
        token,
        body: { mode: "STATELESS", requireApiKey: false }
      });
      targets.push({ id: instance.id, publicUrl: instance.publicUrl, workerKey: instance.workerKey });
    }
  }
  return targets;
}

async function hitTargets(targets) {
  let next = 0;
  const failures = [];
  const workers = Array.from({ length: config.concurrency }, async (_, workerIndex) => {
    while (next < config.requests) {
      const requestIndex = next;
      next += 1;
      const target = targets[requestIndex % targets.length];
      try {
        const tenant = `tenant-${workerIndex}-${requestIndex}`;
        const body = await callMockWithRetry(target, tenant, workerIndex, requestIndex);
        assert(body.source === "autoscaler-demo", "Mock response must come from demo spec", body);
      } catch (error) {
        failures.push(error);
      }
    }
  });
  await Promise.all(workers);
  if (failures.length > 0) {
    throw new Error(`${failures.length} demo request(s) failed:\n${failures.slice(0, 5).map((error) => `- ${error.message}`).join("\n")}`);
  }
}

async function callMockWithRetry(target, tenant, workerIndex, requestIndex) {
  let lastError = null;
  for (let attempt = 0; attempt < 4; attempt += 1) {
    try {
      return await api(`${target.publicUrl}/orders?tenant=${encodeURIComponent(tenant)}`, {
        headers: {
          "X-Trace-Id": `demo-${workerIndex}-${requestIndex}`,
          "X-Forwarded-For": `10.222.${workerIndex % 200}.${(requestIndex % 200) + 20}`
        }
      });
    } catch (error) {
      lastError = error;
      if (!String(error.message).includes("503")) {
        break;
      }
      await sleep(500 * (attempt + 1));
    }
  }
  throw lastError;
}

async function liveWorkers() {
  const workers = await api("/api/runtime/workers", { token });
  return workers.filter((worker) => worker.status === "UP" || worker.status === "DEGRADED");
}

async function waitForSlotSpread(expectedWorkersWithSlots) {
  const startedAt = Date.now();
  while (Date.now() - startedAt < config.waitScaleMs) {
    const live = await liveWorkers();
    const withSlots = live.filter((worker) => worker.slotCount > 0);
    if (withSlots.length >= expectedWorkersWithSlots) {
      return withSlots;
    }
    await sleep(1_000);
  }
  const live = await liveWorkers();
  throw new Error(`Timed out waiting for slots on ${expectedWorkersWithSlots} worker(s): ${JSON.stringify(live.map((worker) => ({ workerKey: worker.workerKey, slots: worker.slotCount })))}`);
}

async function rebalanceRuntime() {
  return api("/api/runtime/rebalance", {
    method: "POST",
    token,
    headers: { "X-MSaaS-Internal-Secret": process.env.AUTOSCALER_INTERNAL_SECRET ?? "local-runtime-secret" },
    body: {}
  });
}

async function waitForTargetAssignments(targets, expectedWorkerCount) {
  const startedAt = Date.now();
  while (Date.now() - startedAt < config.waitScaleMs) {
    const refreshed = await Promise.all(targets.map(async (target) => {
      const instance = await api(`/api/instances/${target.id}`, { token });
      target.workerKey = instance.workerKey;
      return target;
    }));
    const assignedWorkers = new Set(refreshed.map((target) => target.workerKey).filter(Boolean));
    if (assignedWorkers.size >= expectedWorkerCount) {
      return assignedWorkers;
    }
    await sleep(1_000);
  }
  throw new Error(`Timed out waiting for target assignments across ${expectedWorkerCount} workers`);
}

async function waitForLiveWorkers(expected) {
  const startedAt = Date.now();
  while (Date.now() - startedAt < config.waitScaleMs) {
    const live = await liveWorkers();
    if (live.length >= expected) {
      return live;
    }
    await sleep(1_000);
  }
  throw new Error(`Timed out waiting for ${expected} live runtime workers`);
}

function startAutoscaler() {
  const env = {
    ...process.env,
    AUTOSCALER_MIN_WORKERS: String(config.initialWorkers),
    AUTOSCALER_MAX_WORKERS: String(config.targetWorkers),
    AUTOSCALER_SCALE_STEP: String(config.targetWorkers - config.initialWorkers),
    AUTOSCALER_MAX_SLOTS_PER_WORKER: "20",
    AUTOSCALER_SLOT_RATIO_PERCENT: String(Math.max(1, Math.min(100, Math.round((config.slotThreshold / 20) * 100)))),
    AUTOSCALER_INTERVAL_MS: "1000",
    AUTOSCALER_COOLDOWN_MS: "10000",
    AUTOSCALER_TOKEN: token,
    AUTOSCALER_REBALANCE: "true",
    AUTOSCALER_INTERNAL_SECRET: process.env.AUTOSCALER_INTERNAL_SECRET ?? "local-runtime-secret"
  };
  const child = spawn("node", ["scripts/runtime-autoscaler.mjs"], {
    cwd: uiRoot,
    env,
    stdio: ["ignore", "ignore", "ignore"],
    shell: process.platform === "win32"
  });
  child.on("exit", (code) => {
    if (code !== null && code !== 0) {
      console.warn(`Autoscaler exited with code ${code}`);
    }
  });
  return child;
}

function stopAutoscaler() {
  if (autoscaler && !autoscaler.killed) {
    autoscaler.kill();
  }
}

console.log(`MSaaS autoscaler demo started. run=${runId}`);
console.log(`Slot pressure target: ${config.slotsPerWorker} slots per initial worker, threshold=${config.slotThreshold} slots per worker.`);
console.log(`Resetting runtime baseline to ${config.initialWorkers} worker(s).`);
await runDockerCompose(["up", "-d", "--scale", `runtime=${config.initialWorkers}`]);

try {
  await waitForHealth();
  const user = await registerUser();
  console.log(`Registered demo user ${user.username ?? user.email}.`);
  await waitForLiveWorkers(config.initialWorkers);

  autoscaler = startAutoscaler();
  await sleep(2_000);

  const targets = await createTargets();
  console.log(`Published ${targets.length} demo instance(s). Sending load...`);
  await hitTargets(targets);

  const live = await waitForLiveWorkers(config.targetWorkers);
  console.log(`Scale-up confirmed: live runtime workers=${live.length}. Sending post-rebalance load...`);
  const rebalance = await rebalanceRuntime();
  console.log(`Rebalance result: reassigned=${rebalance.reassignedCount} distribution=${JSON.stringify(rebalance.distribution)}`);
  await waitForTargetAssignments(targets, config.targetWorkers);
  await hitTargets(targets);
  const spread = await waitForSlotSpread(config.targetWorkers);
  console.log(`Autoscaler demo passed: live runtime workers=${live.length}; workers with slots=${spread.length}.`);
} finally {
  stopAutoscaler();
}
